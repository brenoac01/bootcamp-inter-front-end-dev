// tipos de função

// declarativas

function funcaoa() {
    console.log('Sou uma mensagem de uma função declarativa')
}

funcaoa();


// Expressões de Funções
// com nomeação

var funcaob = function () {
    console.log('Sou uma mensagem de função de expresão!')
}

funcaob();

// Arrow function

var funcaoc = () => {
    console.log('Sou uma arrow function!')
}

funcaoc();

